    private void jBtnSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvar1ActionPerformed
        if(verificarCampus()){
            Cargos paux = new CargoDao().getCargo(jTextNome.getText());
            if(paux == null || jTextCod.getText().equals((String.valueOf(paux.getId())))){
                Cargos cargo = new Cargos(jTextNome.getText());
                if(jTextCod.getText().equals("")){
                    new CargoDao().save(cargo);
                    new AuditoriaDao().save(cargo, "save");
                }else{
                    cargo.setId(Integer.valueOf(jTextCod.getText()));
                    new CargoDao().update(cargo);
                    new AuditoriaDao().save(cargo, "update");
                }
                desabilitar(true, true, false, false, false, false);
                limparCampus();
                addCargosTabela();
                JOptionPane.showMessageDialog(this, "Salvo com Sucesso!");
            }else{
                  JOptionPane.showMessageDialog(this, "Cargo j√° cadastrado!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Preecha todos os Campos");
        }
    }//GEN-LAST:event_jBtnSalvar1ActionPerformed

