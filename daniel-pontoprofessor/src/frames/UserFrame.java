    private void eventoTabela(){
         int linha = jTable1.getSelectedRow();
         if(linha != -1){
            desabilitar(true, true, false, true, true, false);
            jTextCod.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            jTextNome.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            jTextEmail.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
            jComboPermissao.setSelectedItem(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
         }else{
             limparCampus();
         }
    }
    
    private boolean verificarCampus(){
        if(jTextNome.getText().equals("")){
            jTextNome.requestFocus();
            return false;
        }else if(jTextEmail.getText().equals("")){
            jTextEmail.requestFocus();
            return false;
        }else if(jTextPassword.getText().equals("")){
            jTextPassword.requestFocus();
            return false;
        }
        return true;
    }
    
    private Users verificarEmail(){
        return new UserDao().getUser(jTextEmail.getText());
    }
    
    private void desabilitar(boolean pesquisar, boolean novo,boolean salvar, 
            boolean alterar, boolean excluir, boolean cancelar){
        jBtnPesquisar.setEnabled(pesquisar);
        jBtnNovo.setEnabled(novo);
        jBtnSalvar.setEnabled(salvar);
        jBtnAlterar.setEnabled(alterar);
        jBtnExcluir.setEnabled(excluir);
        jBtnCancelar.setEnabled(cancelar);
        
    }
    
    private void limparCampus(){
        jTextNome.setText("");
        jTextEmail.setText("");
        jTextCod.setText("");
        jTextPassword.setText("");
        jComboPermissao.setSelectedItem("Sim");
    }
    
    private void addUsuariosTabela(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        List<Users> lista =  new UserDao().lista();
        for(Users u : lista){
             Object[] row = { u.getId(), u.getNome(), u.getEmail(), u.getPermissaoroot()};
             model.addRow(row);
        }
        jTable1.setModel(model);
    }
    
    private void pesquisar(){
        String texto = jTextNome.getText();
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        List<Users> lista =  new UserDao().lista(texto);
        for(Users u : lista){
             Object[] row = { u.getId(), u.getNome(), u.getEmail(), u.getPermissaoroot()};
             model.addRow(row);
        }
        jTable1.setModel(model);
    }

