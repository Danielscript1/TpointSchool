    private void jBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvarActionPerformed
        if(verificarCampus()){
            Users uaux = verificarEmail() ;
           if(uaux == null || jTextCod.getText().equals((String.valueOf(uaux.getId())))){
            Users users = new Users(jTextNome.getText(), jTextEmail.getText(), jTextPassword.getText()
                    , "1", jComboPermissao.getSelectedItem().toString());
            if(jTextCod.getText().equals("")){
                 new UserDao().save(users);
                 new AuditoriaDao().save(users, "save");
            }else{
                users.setId(Integer.valueOf(jTextCod.getText()));
                new UserDao().update(users);
                new AuditoriaDao().save(users, "update");
            }
             desabilitar(true, true, false, false, false, false);
             limparCampus();
             addUsuariosTabela();
             JOptionPane.showMessageDialog(this, "Salvo com Sucesso!");
           }else{
                JOptionPane.showMessageDialog(this, "Email já cadastrado!");
           }
       }else{
           JOptionPane.showMessageDialog(this, "Preecha todos os Campos");
       }
    }//GEN-LAST:event_jBtnSalvarActionPerformed

