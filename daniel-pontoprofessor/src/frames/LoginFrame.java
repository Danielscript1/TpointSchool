    private boolean verificarCampos(){
         if(jTextEmail.getText().equals("")){
            jTextEmail.requestFocus();
            return false;
        }else if(jTextPassword.getText().equals("")){
            jTextPassword.requestFocus();
            return false;
        }
        return true;
    }
    private void jBtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEntrarActionPerformed
          
        if(verificarCampos()){
            String email = jTextEmail.getText();
            String password = jTextPassword.getText();
            Users user = new UserDao().login(email, password);
            if(user != null){
                MenuPrincipal menuPrincipal = new MenuPrincipal();
                Util.usuarioLogado = user;
                new AuditoriaDao().save(null, "login");
                 menuPrincipal.setVisible(true);
                this.hide();
            }else{
                JOptionPane.showMessageDialog(this, "Email ou Senha Inv√°lido!");
            }
        }
          
    }//GEN-LAST:event_jBtnEntrarActionPerformed

